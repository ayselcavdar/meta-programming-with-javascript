function myFunc() {
  console.log("hello");
}
myFunc();

//function without parameters
function addTwoNums() {
  var a = 10;
  var b = 20;
  var c = a + b;
  console.log(c);
}
addTwoNums();

//function with parameters
function addTwoNums(a, b) {
  var c = a + b;
  console.log(c);
}
addTwoNums(10, 20);
addTwoNums(5, 9);
